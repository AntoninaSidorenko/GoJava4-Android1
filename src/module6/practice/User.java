public class User {
    private long id;
    private String firstName;
    private String lastName;
    private String city;

    public User(long id, String firstName, String lastName, String city) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.city = city;
    }
}
